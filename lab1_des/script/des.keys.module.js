function des_createKeys (key) {
  const iterations = 1; // Встановимо кількість ітерацій (можна збільшити при використанні DES3)
  const keys = new Array (32 * iterations);
  const shifts = new Array (0, 0, 1, 1, 1, 1, 1, 1, 0, 1, 1, 1, 1, 1, 1, 0); // Задамо зсуви
  let lefttemp, righttemp, m=0, n=0, temp;

  for (let j=0; j<iterations; j++) {
    left = (key.charCodeAt(m++) << 24) | (key.charCodeAt(m++) << 16) | (key.charCodeAt(m++) << 8) | key.charCodeAt(m++);
    right = (key.charCodeAt(m++) << 24) | (key.charCodeAt(m++) << 16) | (key.charCodeAt(m++) << 8) | key.charCodeAt(m++);

    temp = ((left >>> 4) ^ right) & 0x0f0f0f0f; right ^= temp; left ^= (temp << 4);
    temp = ((right >>> -16) ^ left) & 0x0000ffff; left ^= temp; right ^= (temp << -16);
    temp = ((left >>> 2) ^ right) & 0x33333333; right ^= temp; left ^= (temp << 2);
    temp = ((right >>> -16) ^ left) & 0x0000ffff; left ^= temp; right ^= (temp << -16);
    temp = ((left >>> 1) ^ right) & 0x55555555; right ^= temp; left ^= (temp << 1);
    temp = ((right >>> 8) ^ left) & 0x00ff00ff; left ^= temp; right ^= (temp << 8);
    temp = ((left >>> 1) ^ right) & 0x55555555; right ^= temp; left ^= (temp << 1);
    temp = (left << 8) | ((right >>> 20) & 0x000000f0);
    left = (right << 24) | ((right << 8) & 0xff0000) | ((right >>> 8) & 0xff00) | ((right >>> 24) & 0xf0);
    right = temp;

    //проведемо зсуви на п та л ключі
    for (let i=0; i < shifts.length; i++) {
      //зсунимо ключі на 1-2 біти вліво
      if (shifts[i]) {left = (left << 2) | (left >>> 26); right = (right << 2) | (right >>> 26);}
      else {left = (left << 1) | (left >>> 27); right = (right << 1) | (right >>> 27);}
      left &= -0xf; right &= -0xf;
      // застосуємо вибір перестановки ключа 2 (PC2)
      lefttemp = pc2_0[left >>> 28] | pc2_1[(left >>> 24) & 0xf]
              | pc2_2[(left >>> 20) & 0xf] | pc2_3[(left >>> 16) & 0xf]
              | pc2_4[(left >>> 12) & 0xf] | pc2_5[(left >>> 8) & 0xf]
              | pc2_6[(left >>> 4) & 0xf];
      righttemp = pc2_7[right >>> 28] | pc2_8[(right >>> 24) & 0xf]
                | pc2_9[(right >>> 20) & 0xf] | pc2_10[(right >>> 16) & 0xf]
                | pc2_11[(right >>> 12) & 0xf] | pc2_12[(right >>> 8) & 0xf]
                | pc2_13[(right >>> 4) & 0xf];
      temp = ((righttemp >>> 16) ^ lefttemp) & 0x0000ffff; 
      keys[n++] = lefttemp ^ temp; keys[n++] = righttemp ^ (temp << 16);
    }
  }
  return keys;
}